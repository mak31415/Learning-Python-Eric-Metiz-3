def show_messages(messages):
    """Выводит текст каждого сообщения из списка."""
    for message in messages:
        print(message)

def send_messages(messages, sent_messages):
    """
    Перемещает каждое сообщение из одного списка в другой, 
    выводя текст сообщения.
    """
    while messages:
        current_message = messages.pop(0)
        print(f"Отправляется сообщение: {current_message}")
        sent_messages.append(current_message)

# Исходный список сообщений
messages_list = [
    "Привет, как дела?",
    "Не забудь про встречу завтра!",
    "Поздравляю с праздником!",
    "Напиши, как будешь свободен.",
    "Увидимся вечером!"
]

# Пустой список для отправленных сообщений
sent_messages_list = []

# Создание копии списка сообщений
messages_copy = messages_list[:]

# Вызов функции send_messages для копии списка
send_messages(messages_copy, sent_messages_list)

# Вывод списков после выполнения функции
print("\nИсходный список сообщений:")
print(messages_list)

print("\nСписок отправленных сообщений:")
print(sent_messages_list)
