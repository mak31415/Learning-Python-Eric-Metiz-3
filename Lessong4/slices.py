pizzas = ['pepperoni', 'mushrooms', 'extra cheese', 'pineapple', 
         'olives', 'bacon', 'sausage']

for pizza in pizzas[0:3]:
  print("The first three points are: " + pizza)

print("\n")

for pizza in pizzas[2:5]:
  print("The middle three points are: " + pizza)

print("\n")


for pizza in pizzas[4:]:
  print("The last three points are: " + pizza)

print("\n")

